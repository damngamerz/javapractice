public class multiply {
	public static void main (String []args) {
	int x;
	x=5;
	System.out.println("Value of x is: "+x);
	x=x*3;
	System.out.println("Value of x after multiplication is: "+x);
	x=x-3;
	System.out.println("Value of x after subtraction is: "+x);
	x=x+3;
	System.out.println("Value of x after addition is: "+x);
	x=x/3;
	System.out.println("Value of x after division is: "+x);
	}
}
